/**
 * @fileOverview Convert stickers to images.
 */
import { downloadMediaMessage } from '@whiskeysockets/baileys';

export default {
  name: "toimage",
  aliases: ["toimg", "img"],
  category: "converter",
  description: "Convert a sticker into a standard image file.",
  usage: "toimage (reply to sticker)",
  cooldown: 5,
  permissions: 1,
  execute: async (ctx) => {
    const botName = await ctx.bot.managers.settings.get('core', 'name') || ctx.bot.config.name;
    const quoted = ctx.msg.message?.extendedTextMessage?.contextInfo?.quotedMessage;

    if (!quoted?.stickerMessage) {
      return ctx.reply(`в”Ңв”Җв”ҖвҢҲ вҡ пёҸ ERROR вҢӢ\nв”ғ Please reply to a sticker.\nв””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`);
    }

    const { key } = await ctx.reply(`в”Ңв”Җв”ҖвҢҲ рҹ“ё CONVERTING вҢӢ\nв”ғ Task: Sticker вһ” Image\nв”ғ Status: Processing...\nв””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`);

    try {
      const buffer = await downloadMediaMessage(ctx.msg, 'buffer', {});
      
      await ctx.sock.sendMessage(ctx.jid, { 
        image: buffer,
        caption: `в”Ңв”Җв”ҖвҢҲ вң… SUCCESS вҢӢ\nв”ғ Format: Image/PNG\nв”ғ Status: Rendered\nв””в”Җ рҹҢҢ ${botName.toUpperCase()}`
      }, { quoted: ctx.msg });

      await ctx.sock.sendMessage(ctx.jid, { delete: key });
    } catch (e) {
      ctx.reply(`в”Ңв”Җв”ҖвҢҲ вҡ пёҸ ERROR вҢӢ\nв”ғ Conversion failed. Sticker might be corrupted.\nв””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`);
    }
  }
};
